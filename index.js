function add (a, b) {
return (a + b);
}

function subtract (a, b) {
    return (a - b);
}

function multiply (a, b){
    return (a * b);
}

function divide (a, b) {
    return (a / b);
}

function increment (n) {
    n++
    return (n);
}

function decrement (n) {
    n--
    return (n);
}

function makeInt (n, base) {
    parseInt("9000", 10);
    base = 10;
    return (parseInt(n, base));
}

function preserveDecimal (n) {
    parseFloat(n);
    return (parseFloat(n))
}